from chardata.translation_util import LOCALIZED_CHARACTER_CLASSES

class WrappedChar(object):

    def __init__(self, char):
        self.char = char

    def class_string(self):
        return LOCALIZED_CHARACTER_CLASSES.get(self.char.char_class, '')
